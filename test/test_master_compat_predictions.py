"""
Regression tests for individual neural network architectures.

Each test case bundles a small model config, weights, and expected
predictions under test/data/.  These expected values were generated by
the TF/Keras implementation and verify that the PyTorch code produces
equivalent outputs for each supported architecture variant (affinity,
pan-allele multiply/concat, densenet, multi-output).
"""
import json
import os

import numpy as np
import pytest
import torch

from mhcflurry.allele_encoding import AlleleEncoding
from mhcflurry.class1_neural_network import Class1NeuralNetwork
from mhcflurry.common import configure_pytorch, load_weights
from mhcflurry.testing_utils import startup, cleanup


MODEL_NAMES = [
    "master_affinity",
    "master_pan_multiply",
    "master_pan_concat",
    "master_densenet",
    "master_multi_output",
]

DATA_DIR = os.path.join(os.path.dirname(__file__), "data")


def setup_module():
    startup()


def teardown_module():
    cleanup()


def _load_model_and_expected(name):
    prefix = name + "_fixture"
    with open(os.path.join(DATA_DIR, f"{prefix}_config.json"), "r") as f:
        config = json.load(f)
    weights = load_weights(os.path.join(DATA_DIR, f"{prefix}_weights.npz"))
    with open(os.path.join(DATA_DIR, f"{prefix}_predictions.json"), "r") as f:
        expected = json.load(f)
    return config, weights, expected


def _predict(config, weights, expected, backend=None):
    if backend is not None:
        configure_pytorch(backend=backend)
    Class1NeuralNetwork.clear_model_cache()

    model = Class1NeuralNetwork.from_config(config, weights=weights)
    peptides = expected["peptides"]

    if "alleles" in expected:
        allele_encoding = AlleleEncoding(
            alleles=expected["alleles"],
            allele_to_sequence=expected["allele_to_sequence"],
        )
    else:
        allele_encoding = None

    return model.predict(peptides, allele_encoding=allele_encoding)


@pytest.mark.parametrize("model_name", MODEL_NAMES)
def test_predictions_match_expected(model_name):
    config, weights, expected = _load_model_and_expected(model_name)
    predicted = _predict(config, weights, expected)

    np.testing.assert_allclose(
        predicted,
        np.array(expected["predictions"], dtype=np.float64),
        rtol=0.01,
        atol=0.0,
    )


@pytest.mark.parametrize("model_name", MODEL_NAMES)
def test_mps_matches_cpu(model_name):
    if not (hasattr(torch.backends, "mps") and torch.backends.mps.is_available()):
        pytest.skip("MPS is not available")

    config, weights, expected = _load_model_and_expected(model_name)
    predicted_cpu = np.asarray(
        _predict(config, weights, expected, backend="cpu"),
        dtype=np.float64,
    )
    predicted_mps = np.asarray(
        _predict(config, weights, expected, backend="mps"),
        dtype=np.float64,
    )

    np.testing.assert_allclose(predicted_mps, predicted_cpu, rtol=1e-5, atol=1e-2)
